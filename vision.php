				<h4 class="sub-head">Always Ahead Of His Times</h4>
				<p>The great author Jonathan Swift once said, “Vision is the art of seeing what is invisible to others…” and one look at former Aviation Minister Praful Patel’s achievements would prove beyond a doubt that the saying was meant to describe people like the dynamic Patel.</p>

				<p>To say that Praful Patel’s tenure was the Golden Age of Indian civil aviation would be an understatement. Statistics support the fact that domestic air traffic has increased more than three times since Patel took charge. There has also been huge growth in international traffic as Patel brought in reforms, which proved to be a major boost for the civil aviation industry. In 2004, India had a total of 125 aircrafts for civil aviation but after Patel took charge, the Indian civil aviation sector now has more than 400 aircrafts!</p>
				<p>Apart from this, Patel also ensured that Indian aviation benefited like never before when he started focusing on airports in the country. Braving stiff opposition, he also managed to privatize the Delhi, Mumbai, Hyderabad and Bangalore airports — the busiest in the country, to enable major expansion of facilities and induction of large investment along with private sector participation.</p>

				<p>Delhi’s Indira Gandhi Airport T3, which is considered the one of the world’s largest and one of the – most sophisticated and modern airports, was a result of Praful Patel’s vision, who built India’s largest public building since Independence. The upcoming new international terminal T2 at Mumbai will rank as the finest in the world. Apart from that, during his seven-year term, he started and finished many Airport building projects in India. It was during Patel’s tenure that modern airports across the country were inaugurated. For those who do not know this, the Hyderabad International Airport features as the top among Indian airports in the ranking of world airports for the year 2013 by an UK based aviation research organization. Within months of taking charge, Patel convinced the government to hike the FDI cap in the domestic airlines sector from 40% to 49%. It is widely acknowledged by all that Praful Patel single-handedly contributed in making Indian aviation a force to be reckoned with.</p>

				<p>To understand how one man accomplished this single-handedly, one has to go back in time when Praful Patel was just a teenager. After the demise of his father Manoharbhai, Praful Patel learnt from quite a young age that ‘with great power, comes great responsibility’ and the college-going Praful took on the responsibility of looking after the family business as well as the social work that his father had been doing. Needless to say, shouldering such responsibilities at a young age instilled a sense of duty and commitment in him, which was to be proved in more ways than one as he went on to tackle bigger challenges in life.</p>

				<p>No wonder then, that when he won his first Lok Sabha Election in 1991 at the young age of 33, he was already an able administrator and a dynamic ‘go-getter’ by then. Patel had a meteoric rise in the Nationalist Congress Party and when he was made the Minister of Civil Aviation, it was taken for granted by everyone that the field of aviation was to undergo some major developmental changes…and it did! On taking up the reins of the Indian aviation sector, Praful Patel completely revitalised it with admirable results, which are there for all to see. There was growth in air traffic, which also led to many challenges. But Patel dealt with those challenges with the vision of a progressive leader. He managed to upgrade and strengthen the entire air traffic system. Today, India boasts of the most advanced and modernised ATC and CNS systems in the world. India stands fourth in the world to have its own satellite-based navigation system. In his tenure, the Airport Authority of India launched its own satellite under the GAGAN program. After changing the face of civil aviation in the country, on January 19, 2011, Praful moved on to the Ministry of Heavy Industries and Public Enterprises and started major reforms. If the move was a loss for India’s aviation sector, it proved to be a boon for the Heavy Industries sector.</p>

				<p>On 9th January 2013, Patel launched the National Electric Mobility Mission Plan (NEMMP) 2020 which is a roadmap for progressive induction and expansion of efficient and environmentally friendly electric and hybrid vehicles in the country. The NEMMP 2020 will ensure that India achieves high level of usage of electric vehicles, which would be both cost-efficient and help the country save foreign exchange against our huge import of fuel requirements. In July 2013, Patel also launched The Indian Electrical Equipment Industry Mission Plan (IEEIMP) 2022 with a specific intention to ensure that Indian manufacturers are able to not only fulfill our requirements, but also to be major exporters of electrical equipment. Two important areas namely – the Auto Sector Skill Development Council and the Capital Goods Sector Skill Council were also promoted. In this period, Bharat Heavy Electricals Limited (BHEL) achieved a record turnover of Rs. 50,000 crores and was accorded the Maha Ratna status.</p>

				<p>Through the Department of Public Enterprises, Patel carried out many reforms to ensure a level playing field to the public sector vis-à-vis private sector in a highly competitive market scenario, both domestic and global. A high-level Committee was appointed, followed by a Group of Ministers, which has made key suggestions for the future role of the public sector. On the lines of the Prime Minister regularly meeting with the captains of the private sector industry, the Prime Minister also met the Chief Executives of major public sector undertakings for the first time. This has encouraged public sector undertakings. The Department of Public Enterprises guidelines on MOU, Corporate Governance, Research and Development, Human Resources Development and Corporate Social Responsibility have been given high priority.</p>

				<p>Among the many prestigious awards, Patel has been bestowed a doctorate by the prestigious Leeds Metropolitan University for his contribution in making India a rapidly developing and economically strong nation in 2007. Besides this, in the same year, he was awarded Reformer of the Year by Asia’s largest financial daily The Economic Times. However, when asked about his achievements, the dynamic yet modest leader says with a shrug, “If I decide to do something, I make sure it gets done. I underline the word achievement, but helping people gives me more satisfaction.” This trait of bringing people together and accomplishing things with least hassles convinces his critics and well-wishers in comparing Patel with his political idol Sardar Vallabhbhai Patel.</p>

