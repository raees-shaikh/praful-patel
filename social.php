					<h4 class="sub-hd">Praful Patel : The Leader Who Is One Of Us</h4>
					<p>Responsibility came early as he lost his father when he was 13 years old. The death of his father instilled a sense of duty in the young Patel and 
					in a way gave a reason to live – and to continue the good work whose foundation had been laid by his able father. Till date Patel continues to 
					personify that rare breed of men who make meaningful contribution in every aspect of their lives.</p>

					<p>His philanthropy works and others social activities are truly remarkable, which are reflected in the love and the affection people of the 
					region shower on him. Besides conducting a successful and exemplary political career, Patel has not fallen short of his responsibilities towards 
					the society and the public, in particular. Interestingly, Patel has been actively involved in social and sports activities that form an integral 
					part of his mission of touching people’s lives in varied ways.</p>

					<h4 class="sub-hd">The Reformer…</h4>
					<p>The philanthropy works of his family began with many educational institutions in the Bhandara-Gondia district started by his father. 
					His father started twenty-two schools and a college in a single day in a district which only had one school. A rare achievement in the days when 
					philanthropy was not in fashion. Stepping into his father’s shoes, Praful Patel never forgot what his father had once said to him, “Your first 
					commitment is to society and its development is your prime duty.” Patel family runs Gondia Education Society along with host of several educational 
					and charitable trusts. The Gondia Education Society imparts education to 1,10,000 students in all disciplines ranging from Arts, Commerce, Engineering, 
					Law, Information Technology (IT) and Computer Sciences to Pharmacy. Apart from Gondia Education Society, Patel family a staunch believer in the importance 
					of education, also founded the TheNadiad Education Society in Gujarat, which imparts education in all vocational and graduation courses.</p>					

					<p>He credits his father for these initiatives. He states, “My father has given more to the district than to his family. He is my only inspiration in 
					life.” As a way to reach the people’s hearts he proposed and executed the unique idea of collective marriage among people of different religions under 
					one roof known as Manoharbhai Patel Academy. More than 3,000 young couples celebrated their marriage in this initiative in Gondia and Bhandara. 
					His trust has successfully conducted open heart surgeries for more than 3,000 needy patients, thousands of people every year undergo medical treatment 
					for different ailments and there are many camps for the disabled. There are many such substantial and valuable contributions Patel has made in his 
					formidable career as a politician. Through these apolitical activities there is only one goal Patel has in mind: creating strong bonding, peace and 
					harmony amongst people of the society.</p>

