					<p>The Nationalist Congress Party (NCP) is a true inheritor of the rich and glorious political legacy handed to the Indian National Congress (INC) 
					by early stalwarts like Dadabhai Naoroji, Gopal Krishna Gokhale, Bal Gangadhar Tilak and Annie Besant, and by other veterans of the freedom struggle – 
					The Father of the Nation Mahatma Gandhi himself, Pandit Jawaharlal Nehru, Maulana Abul Kalam Azad, Sardar Vallabhbhai Patel, Aurobindo, Subhash Chandra Bose, 
