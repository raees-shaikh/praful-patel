					<p>It was on 25th May 1999 that the Nationalist leaders Sharad Pawar, Praful Patel and Shri Tariq Anwar with hundreds of their political supporters 
					assembled at No. 6, Gurudwara Rakab Ganj Road, New Delhi and took the historic decision to form a new Political Party under the name ‘Nationalist Congress Party’.
					Sharad Pawar was elected President and Praful Patel and Shri Tariq Anwar, General Secretaries of the party.</p>					

					<p>NCP is the Millennial Party with a modern and progressive orientation. Our ideology is of a holistic democracy anchored on Gandhian secularism, 
					equity, social justice and federalism based National Unity.</p>

					<h4 class="sub-head">NCP’s Founding Principles are…</h4>
					<ul>
						<li>Strengthening the forces of nationalism with an emphasis on the egalitarian and secular ethos of the Indian Republic and combating fundamentalism and sectarianism.Maintaining the unity and integrity of India by strengthening federalism and decentralization of power up to the village level.</li>
						<li>Promoting economic growth through competition, self-reliance, individual initiative and enterprises with emphasis on equality and social justice.</li>
						<li>Rule of law and constitutional order based on Parliamentary and participatory democracy.</li>
						<li>Empowerment of weaker sections, the schedule castes and tribes, OBCs, the disabled and the women.</li>
						<li>Strengthening the forces of peace within the country and attempting to secure universal non-discriminatory disarmament.</li>
						<li>Institutionalized and democratic functioning of the party.</li>
