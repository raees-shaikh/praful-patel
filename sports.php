				<div class="q-col-1-2">
					<p>The tall, athletically built Patel is also an avid sports enthusiast. He serves as the President of All India Football Federation and Member 
					Executive Committee Asian Football Confederation. Through this association, he intends to keep the sporting spirit in people alive. 
					He believes that sports are the best way to build character and instill qualities like teamwork, dedication and discipline. 
					He has great passion for games like hockey, cricket, badminton and tennis, having played most of these games at the school and college level.</p>

					<p>Praful Patel thoroughly exemplifies the spirit of a sportsman who has never-say-die attitude in achieving his goal and developing a strong resolve to take in his stride whatever challenge comes in his way. Besides conducting a successful political career, Patel has not fallen short of his responsibilities towards society and the public. He has been actively involved in many sports activities that form an integral part of his mission of touching people’s lives in varied ways.</p>

					<p>With the aim of reviving football in India and bringing back the glory of the game of the 50’s and 60’s, Praful Patel took on the responsibility of the All India Football Federation (AIFF). A task full of challenges and amidst a very turbulent environment he set about putting the house in order.</p>

					<p>He always says “Indian football can never come up until we strengthen the grass roots. A strong youth development program will have to be launched, if we are to build and sustain the future of football in our country. There will be many critics but we shall not be deterred.” With this in mind the AIFF has launched Mission Lakshya 2022.</p>

					<p>Patel has successfully launched AIFF Academies under which he has established three Regional Academies in Navi Mumbai in Mumbai, Kolkata and Goa and an Elite Academy in Goa. In coordination with Federation Internationale de Football Association (FIFA), AIFF installed eight Artificial Turfs in Mumbai, Bangalore, Imphal, Shillong, Goa, Barasat near Kolkata, Andamans and Port Blair. AIFF has conducted the first-ever Pro-License Coaching Course in India in July 2013.</p>

					<p>India has bid for the U-17 World Cup in 2017, an Event aimed at creating World Class infrastructure and further broadcasting Sport in the Country.</p>
