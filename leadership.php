					which he founded in Gondia. Being himself literate only upto class IV, his first mission in life was education and thus he always remained a philanthropist 
					and social activist at heart. And in these roles, the able support and the driving force behind his success, was his wife Shantaben. 
					Imbibing high and humanist values from these two important people of his life, Praful Patel, who is one of the most influential Cabinet Ministers of India, 
					has not lost the ability of bonding with the common masses– it is not an uncommon sight to see the dynamic Patel deal with the farmers from his home districts 
					of Bhandara and Gondia with effortless ease.</p>
				</div>
				<div class="q-col-1-1">
				<p>Being the son of a leader himself, Patel took to politics at a young age and was first elected as President of – Municipal Council of Gondia at a young age 
