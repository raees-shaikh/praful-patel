					<p>Born and brought up in a well-to-do family, Praful Patel nevertheless represents a perfect mix of power and humility, all thanks to the 
					teachings of his father, the late Manoharbhai Patel, who himself came from a very humble beginning. His father, Manoharbhai Patel could not pursue 
					his education as he came from a very poor family and left his native Gujarat for Central India in search of employment. From loading and unloading 
