				<div class="q-col-1-2">					
					<p>પ્રફુલ પટેલનો જન્મ 1957ની 17 ફેબ્રુઆરીએ કોલકાતામાં થયો હતો. શ્રી પ્રફુલ પટેલ 33 વર્ષની યુવાન વયે સૌપ્રથમ વખત લોકસભામાં ચૂંટાઈ આવ્યા હતા. તેમના નેતૃત્વની લોકપ્રિયતા એટલી બધી હતી કે 10મી, 11મી તેમજ 12મી એમ સળંગ ત્રણ વખત તેઓ લોકસભામાં ચૂંટાયા હતા. ત્યારબાદ 2009માં ચોથી વખત પણ તેઓ 15મી લોકસભામાં ચૂંટાઈ આવ્યા હતા. વચ્ચેના ગાળામાં એટલે કે વર્ષ 2000 તથા 2006માં એમ બે વખત તેઓ રાજ્યસભાના સાંસદ તરીકે પણ ચૂંટાયા હતા.</p>

					<p>ત્રણ દાયકા કરતાં વધુ સમયની સંસદીય કારકિર્દી દરમિયાન શ્રી પ્રફુલ પટેલે નાણાં, નાગરિક ઉડ્ડયન, પર્યાવરણ અને વન તેમજ વિદેશી બાબતો જેવી ઘણી સંસદીય સમિતિમાં સભ્ય તરીકે સેવા આપી હતી. તેઓ ભારત-યુકે સંસદીય ફોરમના ચેરમેન હતા તેમજ ભારત-અમેરિકા સંસદીય ફોરમના સ્થાપક સભ્ય હતા.</p>

					<p>નાગરિક ઉડ્ડયન મંત્રાલયની શ્રેષ્ઠ કામગીરી બદલ આપવામાં આવતો સીએપીએ (સેન્ટર ફૉર એશિયા પેસિફિક એવિએશન) એવોર્ડ શ્રી પ્રફુલ પટેલને 2005માં એનાયત થયો હતો. આ એવોર્ડ એશિયા પેસિફિક ક્ષેત્રના એ કેન્દ્રીય પ્રધાનને એનાયત કરવામાં આવે છે જેમણે આ ક્ષેત્રમાં એવિએશન માટે શ્રેષ્ઠ કામગીરી કરી હોય. અગ્રણી ન્યૂઝ મેગેઝિન ઈન્ડિયા ટુડેએ 2006માં શ્રી પ્રફુલ પટેલને નંબર વન મિનિસ્ટરનો રેન્ક આપ્યો હતો. 2007માં શ્રી પટેલને ઈકોનોમિક ટાઈમ્સ રિફોર્મર ઑફ યરનું સન્માન મળ્યું હતું.</p>
