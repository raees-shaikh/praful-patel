					<p>સ્વ. મનોહરભાઈ પટેલના પુત્ર શ્રી પ્રફુલ પટેલ કોમર્સમાં સ્નાતક થયા પછી પોતાના પારિવારિક બિઝનેસમાં જોડાયા હતા. સીજે ગ્રુપના નામ હેઠળ તેમનો પારિવારિક બિઝનેસ બીડી, તમાકુ, ફાર્માસ્યૂટિકલ, ફાઈનાન્સ, રિયલ એસ્ટેટ, પેકેજિંગ જેવા વિવિધ ક્ષેત્રમાં સંકળાયેલો છે.</p>

					<p>શ્રી પ્રફુલ પટેલને રાજકારણ અને સમાજસેવાનો વારસો તેમના પિતા તરફથી મળ્યો હતો. તેમના પિતા મનોહરભાઈ પટેલ મહારાષ્ટ્રમાં કોંગ્રેસના વરિષ્ઠ નેતા હોવા ઉપરાંત એક મહાન સમાજસેવક હતા. પટેલ પરિવાર મહારાષ્ટ્રના ગોંદિયા જિલ્લામાં ‘ગોંદિયા એજ્યુકેશન સોસાયટી’નું સંચાલન કરે છે, જેના નેજા હેઠળ આર્ટ્સ, કોમર્સ, એન્જિનિયરિંગ, કાયદો, ફાર્મસી, ઈન્ફર્મેશન ટેકનોલોજી, કમ્પ્યૂટર સાયન્સ વગેરે વિદ્યાશાખાઓમાં 1,10,000 કરતાં વધારે વિદ્યાર્થીઓ શિક્ષણ મેળવીને આત્મનિર્ભર થયા છે.</p>
					
					<p>રાજકારણ અને બિઝનેસ ઉપરાંત શ્રી પ્રફુલ પટેલને રમતગમત સાથે પણ એટલો જ લગાવ છે. તેઓ ઑલ ઈન્ડિયા ફૂટબૉલ ફેડરેશનના પ્રમુખ તથા એશિયન ફૂટબૉલ ફેડરેશનની એક્ઝિક્યુટિવ સમિતિના સભ્ય છે. યંગ પ્રેસિડેન્ટ્સ ઓર્ગેનાઈઝેશન (વાયપીઓ)ના સક્રિય સભ્ય હોવા સાથે શ્રી પટેલ વિવિધ સામાજિક સંગઠનો સાથે જાડાયેલા છે.</p>
