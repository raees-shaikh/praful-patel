					<p>દેશની પ્રીમિયર ફૂટબૉલ સ્પર્ધા આઈ-લીગ માટે તંદુરસ્ત વાતાવરણ ઊભું કરવામાં તેમની મુખ્ય ભૂમિકા છે. તેમના નેતૃત્વ હેઠળ બે નવી ક્લબ – બેંગાલુરુ એફસી તથા મુંબઈ ટાઈગર્સને 2013-14ની આઈ-લીગ સીઝનમાં પ્રવેશ મળ્યો હતો. એશિયન ફૂટબૉલ કન્ફેડરેશન (એએફસી)ની અંડર-13 ગર્લ્સ ફૂટબૉલ ટુર્નામેન્ટ જે કોલંબોમાં યોજાઈ હતી તેને પ્રોત્સાહિત કરવામાં પ્રફુલ પટેલે નોંધપાત્ર ભૂમિકા ભજવી હતી. મે 2013માં કટકમાં એઆઈએફએફ રિફરિંગ એશિયન ફૂટબૉલ કન્ફેડરેશન એકેડમીનું ઉદઘાટન પણ તેમણે કર્યું હતું.</p>
					
					<p>2012માં ભારતીય સિનિયર નેશનલ ટીમે સતત ત્રીજી વખત નહેરુ કપ જીતી લેતાં મહિલા ટીમ સહિત વિવિધ એજગ્રુપની ટીમોને આ રમત પ્રત્યે ભારે પ્રોત્સાહન મળ્યું છે. હવે ફૂટબૉલનું ગૌરવ પાછું મળે એ જ લક્ષ્યાંક છે.</p>

