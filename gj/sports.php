			<h2 class="section-headline wow animated fadeInLeft animated center-txt">ઊર્જાવાન સ્પોર્ટ્સમેન</h2>
				<div class="q-col-1-2">
				<img alt="" src="assets/img/praful-patel-sports1.jpg">					
				</div>
				<div class="q-col-1-2">
					<p>ઊંચાઈ અને એથ્લેટિક શરીર સૌષ્ઠવ ધરાવતા પ્રફુલ પટેલ રમતગમતમાં પણ એટલો જ ઉત્સાહ ધરાવે છે. તેઓ ઑલ ઈન્ડિયા ફૂટબૉલ ફેડરેશનના પ્રમુખ તરીકે સેવા આપે છે તે ઉપરાંત એશિયન ફૂટબૉલ કન્ફેડરેશનની કારોબારી સમિતિના સભ્ય છે. આ સંગઠનો દ્વારા લોકોમાં રમત પ્રત્યેની ભાવના જાગૃત રહે તેમ તેઓ ઈચ્છે છે. તેઓ માને છે કે ચરિત્ર નિર્માણ માટે રમત સૌથી શ્રેષ્ઠ બાબત છે. તેને કારણે ટીમવર્કની ભાવના પેદા થાય છે અને વ્યક્તિમાં પ્રતિબદ્ધતા અને શિસ્ત આવી શકે છે. હોકી, ક્રિકેટ, બેડમિંટન તથા ટેનિસ જેવી રમતો માટે તેમને ભારે લગાવ છે. આંમાની મોટાભાગની રમતોમાં તેમણે સ્કૂલ અને કૉલેજ લેવલે ભાગ લીધો હતો.</p>

					<p>પ્રફુલ પટેલમાં એક સ્પોર્ટ્સમેન તરીકેનો સ્પિરિટ કાયમ જોવા મળે છે જે સતત ઝઝૂમવામાં અને ધ્યેય હાંસલ કરવામાં માને છે. સફળ રાજકીય કારકિર્દીની સાથે સાથે તેમણે સમાજ પ્રત્યેની જવાબદારી પણ બખૂબી અદા કરી છે એટલું જ નહીં પરંતુ રમતગમતને પ્રોત્સાહન આપવામાં પણ ખૂબ આગળપડતી કામગીરી કરી છે.</p>

					<p>ભારતમાં ફૂટબૉલની રમતને 1950 અને 1960ના દાયકા જેવું ગૌરવ પ્રાપ્ત થાય તે માટે તેમણે ઑલ ઈન્ડિયા ફૂટબૉલ ફેડરેશન (એઆઈએફએફ)ની જવાબદારી લીધી છે. આ કામગીરી ઘણી પડકારજનક છે અને સમગ્ર વ્યવસ્થાતંત્રને સુધારવાનો પણ પડકાર છે.</p>

					<p>તેઓ હંમેશાં કહે છે કે, “જ્યાં સુધી આપણે પાયાના સ્તરને મજબૂત ના બનાવીએ ત્યાં સુધી ભારતીય ફૂટબૉલ આગળ આવી શકે નહીં. જો આપણા દેશમાં ફૂટબૉલનું ભવિષ્ય સુધારવું હોય તો યુવાનોના વિકાસનો એક મજબૂત કાર્યક્રમ શરૂ કરવો પડે. આ કામગીરીની ઘણા લોકો ટીકા કરશે, પરંતુ આપણે પીછેહઠ કરવી ના જોઈએ.” આ બાબતને ધ્યાનમાં રાખીને એઆઈએફએફ દ્વારા મિશન લક્ષ્ય 2022 શરૂ કરવામાં આવ્યું છે.</p>

					<p>પ્રફુલ પટેલે સફળતાપૂર્વક એઆઈએફએફ એકેડમી શરૂ કરી છે જેના હેઠળ તેમણે મુંબઈમાં નવી મુંબઈ, કોલકાતા અને ગોવામાં એલિટ એકેડમી એમ ત્રણ પ્રાદેશિક એકેડમીની સ્થાપના કરી છે. ફેડરેશન ઈન્ટરનેશનલ ડી ફૂટબૉલ એસોસિયેશન (ફીફા)ના સહયોગમાં એઆઈએફએફ દ્વ્રારા આઠ મુંબઈ, બેંગલોર, ઈમ્ફાલ, શિલોંગ, ગોવા, કોલકાતા નજીક બારાસાત, આંદામાન તેમજ પોર્ટબ્લેર એમ આઠ સ્થળે આર્ટિફિશિયલ ટર્ફ તૈયાર કરવામાં આવી છે. એઆઈએફએફે જુલાઈ 2013માં પ્રથમ વખથ પ્રો-લાઈસન્સ કોચિંગ કોર્સ શરૂ કર્યો હતો.</p>

					<p>ભારતે 2017ના અંડર 17 વર્લ્ડકપ માટે બિડ કરેલું છે. આ ઈવેન્ટથી દેશમાં વિશ્વકક્ષાનું ઈન્ફ્રાસ્ટ્રક્ચર ઊભું થશે તથા આ રમતને વધુ પ્રોત્સાહન મળશે.</p>
