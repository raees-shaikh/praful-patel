					</div>
					<p>પ્રફુલ પટેલ રાષ્ટ્રવાદી કોંગ્રેસ પાર્ટી (એનસીપી)ના પાયાના સ્થાપકો પૈકી એક છે અને તેઓ ગુજરાત સહિત ઘણાં રાજ્યોમાં પક્ષની જવાબદારી સંભાળે છે.</p>

					<p>દાદાભાઈ નવરોજી, ગોપાલકૃષ્ણ ગોખલે, બાલગંગાધર તિળક તથા એની બેસન્ટ જેવા મહાન નેતાઓ દ્વારા ભારતીય રાષ્ટ્રીય કોંગ્રેસ (આઈએનસી)ને જે સમૃદ્ધ અને ભવ્ય રાજકીય વારસો આપવામાં આવ્યો હતો તેનો સાચો વારસો રાષ્ટ્રવાદી કોંગ્રેસ પાર્ટી (એનસીપી) એ જાળવ્યો છે. આ ઉપરાંત એનસીપીએ સ્વતંત્રતા ચળવળના અગ્રણીઓ – રાષ્ટ્રપિતા મહાત્મા ગાંધી, પંડિત જવાહરલાલ નહેરુ, મૌલાના અબુલકલામ આઝાદ, સરદાર વલ્લભભાઈ પટેલ, શ્રી અરવિંદ, સુભાષચંદ્ર બોઝ, ખાન અબ્દુલ ગફાર ખાન, શ્રીમતી સરોજીની નાયડુ, મેડમ ભીખાજી કામા તથા અરુણા અસફઅલીનો વારસો પણ જાળવ્યો છે. આ વારસો એટલે પ્રખર રાષ્ટ્રવાદ અને સેક્યુલર મૂલ્યો, જે આપણી ઐતિહાસિક ધરોહરનાં મુખ્ય પરિબળો છે.</p>

					<p>1999ની 25મી મેએ રાષ્ટ્રવાદી નેતાઓ શ્રી શરદ પવાર, શ્રી પ્રફુલ પટેલ અને શ્રી તારીક અનવર ઉપરાંત તેમના હજારો રાજકીય ટેકેદારો 6, ગુરુદ્વારા રકાબગંજ રોડ, નવી દિલ્હી ખાતે એકત્ર થયા હતા અને ‘રાષ્ટ્રવાદી કોંગ્રેસ પાર્ટી’ નામે નવો રાજકીય પક્ષ રચવા ઐતિહાસિક નિર્ણય લીધો હતો. શ્રી શરદ પવાર પક્ષ પ્રમુખ તરીકે ચૂંટાયા હતા અને શ્રી પ્રફુલ પટેલ તથા શ્રી તારીક અનવર મહામંત્રી તરીકે ચૂંટાયા હતા.</p>					

					<p>એનસીપી એ એક એવો પ્રાચીન મૂલ્યો ધરાવતો પક્ષ છે જેની વિચારધારા આધુનિક અને પ્રગતિશીલ છે. અમારી વિચારધારા સેક્યુલારિઝમ, સમાનતા, સામાજિક ન્યાય તથા રાષ્ટ્રીય એકતા પર આધારિત સંઘવાદ જેવા ગાંધીવાદી સિદ્ધાંતો પર આધારિત છે.</p>

					<h4 class="sub-head">એનસીપીના સ્થાપક સિદ્ધાંતો આ પ્રમાણે છે …</h4>
					<ul>
						<li>રાષ્ટ્રવાદી પરિબળોને મજબૂત કરવા અને તેમાં ભારતીય પ્રજાસત્તાકના સેક્યુલર અને સર્વસમાવેશી સિદ્ધાંતો પર ભાર મૂકવો. ગ્રામ્ય સ્તરે સત્તાના વિકેન્દ્રીકરણ દ્વારા સંઘવાદને મજબૂત કરી એકતા અને અખંડિતતા જાળવવી.</li>

						<li>સ્પર્ધા, આત્મનિર્ભરતા, વ્યક્તિગત પહેલ તથા ઉદ્યોગોને પ્રોત્સાહન દ્વારા આર્થિક પ્રગતિ સાધવી જેમાં સમાનતા અને સામાજિક ન્યાય કેન્દ્રમાં હોય.</li>

						<li>સંસદીય અને લોકભાગીદારી વાળી લોકશાહીના આધાર ઉપર કાયદાનું શાસન અને બંધારણીય વ્યવસ્થા સ્થાપવી.</li>

						<li>નબળા વર્ગો, અનુસૂચિતા જાતિઓ તથા આદિવાસીઓ, ઓબીસી, વિકલાંગ તથા મહિલાઓનું સશક્તિકરણ કરવું.</li>

						<li>દેશની અંદર શાંતિ ઈચ્છતા પરિબળોને મજબૂત બનાવવા તથા વૈશ્વિક નિઃશસ્ત્રીકરણ માટે પ્રયાસ કરવો.</li>

						<li>પક્ષનું સંસ્થાનિકરણ તથા લોકશાહી ઢબે કામગીરી કરવી.</li>
					</ul>
