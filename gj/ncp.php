					<h2 class="section-headline wow animated fadeInLeft animated center-txt hd-marg">રાષ્ટ્રવાદી કોંગ્રેસ પક્ષ</h2>
					<div class="q-container"  style="text-align: center;">
						<div class="q-row">
							<div class="q-col-1-1">
								<img alt="" src="assets/img/ncp-flag.jpg" width="60%">
							</div>
