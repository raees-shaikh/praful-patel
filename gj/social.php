					<h4 class="sub-hd">પ્રફુલ પટેલઃ નેતા જે આપણા સૌના છે</h4>
					<p>પ્રફુલ પટેલ માત્ર 13 વર્ષના હતા ત્યારે પિતાની છત્રછાયા ગુમાવતા તેમના પર જવાબદારી આવી પડી હતી. પિતાના મૃત્યુએ યુવાન પ્રફુલમાં જવાબદારીની ભાવના જગાવી અને એ રીતે તેમને જીવનનું ધ્યેય હાંસલ થયું. તેમણે તેમના પિતા દ્વારા શરૂ કરવામાં આવેલી કામગીરી આગળ ધપાવી. પ્રફુલ પટેલ આટલા વર્ષે પણ હજુ એ જ રીતે કામગીરી કરી રહ્યા છે અને તેમણે એક એવી વ્યક્તિ તરીકે આદર્શ ઊભો કર્યો છે જે દરેકના જીવનમાં અર્થપૂર્ણ ભાગ ભજવે.</p>

					<p>તેમના સેવાનાં કામો તેમજ અન્ય સામાજિક પ્રવૃત્તિઓ ખરેખર નોંધપાત્ર છે. તેમના મતક્ષેત્રના પ્રજાજનો તેમના ઉપર જે પ્રેમ અને લાગણી દર્શાવે છે તેના પરથી આ વાતનો અંદાજ આવી શકે છે. એક સફળ અને ઉદાહરણરૂપ રાજકીય કારકિર્દીની સાથે સાથે પ્રફુલ પટેલે તેમની સામાજિક જવાબદારીઓ પણ એટલી જ કૂનેહપૂર્વક અદા કરી છે. રસપ્રદ બાબત એ છ કે તેઓ સામાજિક તેમજ રમતગમતની પ્રવૃત્તિઓમાં સક્રિય રીતે સંકળાયેલા છે.</p>

					<h4 class="sub-hd">એક સુધારક …</h4>
					<p>તેમના પરિવારની સેવા પ્રવૃત્તિ ભંડારા-ગોંદિયા જિલ્લામાં શૈક્ષણિક સંસ્થાઓ સાથે શરૂ થાય છે, જે તેમના પિતાએ શરૂ કરી હતી. તેમના પિતાએ જિલ્લામાં એક જ સ્કૂલ હતી ત્યારે અથાગ પરિશ્રમ કરીને એક જ દિવસમાં 22 શાળાઓ શરૂ કરી હતી. સમાજસેવા જે સમયમાં ફેશન નહોતી તેવા સમયે તેમણે કરેલી આ કામગીરી અસાધારણ ગણાય છે. પ્રફુલ પટેલ હંમેશાં તેમના પિતાના પગલે ચાલ્યા છે અને પિતાએ આપેલી એ શીખ કદી ભૂલ્યા નથી કે, “તારી પ્રથમ પ્રતિબદ્ધતા સમાજ પ્રત્યેની છે અને સમાજનો વિકાસ એ તારી પ્રથમ ફરજ છે.” પટેલ પરિવાર ગોંદિયા એજ્યુકેશન સોસાયટી ઉપરાંત બીજા કેટલાક શૈક્ષણિક તેમજ ચેરિટેબલ ટ્રસ્ટનું સંચાલન કરે છે. ગોંદિયા એજ્યુકેશન સોસાયટીના નેજા હેઠળ આર્ટ્સ, કોમર્સ, એન્જિનિયરિંગ, કાયદો, ફાર્મસી, ઈન્ફર્મેશન ટેકનોલોજી, કમ્પ્યૂટર સાયન્સ વગેરે વિદ્યાશાખાઓમાં 1,10,000 કરતાં વધારે વિદ્યાર્થીઓ શિક્ષણ આપવામાં આવે છે. શિક્ષણના મહત્ત્વને મક્કમતાપૂર્વક માનતો પટેલ પરિવારે ગોંદિયા એજ્યુકેશન સોસાયટી ઉપરાંત ગુજરાતમાં નડિયાદ એજ્યુકેશન સોસાયટીની પણ સ્થાપના કરી છે, જ્યાં તમામ વ્યવસાયી અને ગ્રેજ્યુએશનના અભ્યાસક્રમ ચાલે છે.</p>					

					<p>પ્રફુલ પટેલ આ તમામ પ્રવૃત્તિઓ માટે તેમના પિતાને શ્રેય આપે છે. “મારા પિતાએ પરિવારને આપ્યું તેના કરતાં જિલ્લાને વધારે આપ્યું છે. મારા જીવનમાં તેઓ એકમાત્ર પ્રેરણાસ્રોત છે.” તેમણે કરેલી એક નોંધપાત્ર કામગીરીમાં વિવિધ ધર્મોના લોકોના સામૂહિક લગ્નોની છે. આ કામ તેમણે મનોહરભાઈ પટેલ એકેડમી તરીકે જાણીતા સ્થળે પાર પાડ્યું હતું. આ વિશિષ્ઠ લગ્ન સમારંભમાં ગોંદિયા અ ભંડારાના 3000 કરતાં વધુ દંપતી લગ્નગ્રંથીથી જોડાયાં હતાં. તેમના ટ્રસ્ટે 3000 કરતાં વધુ દર્દીઓની ઓપન હાર્ટ સર્જરી સફળતાપૂર્વક કરાવી છે. આ સિવાય દર વર્ષે હજારો લોકો વિવિધ કેમ્પમાં તબીબી સારવાર કરાવે છે. પ્રફુલ પટેલે રાજકારણી તરીકે આવા અનેક સેવાકાર્ય કર્યા છે. આવી બિનરાજકીય પ્રવૃત્તિઓ દ્વારા તેમનું એક જ ધ્યેય છે કે સમાજવમાં વિવિધ વર્ગો વચ્ચે શાંતિ અને સંવાદિતા મજબૂત કરવી.</p>

