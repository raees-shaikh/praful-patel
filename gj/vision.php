					<h4 class="sub-head">હંમેશાં સમય કરતાં આગળ…</h4>
					<p>મહાન લેખક જોનાથન સ્વિફ્ટ કહે છે કે, “વિઝન એ એવું જોવાની કળા છે જે બીજા માટે અદૃશ્ય હોય છે…” અને જો ભૂતપૂર્વ ઉડ્ડયનપ્રધાન પ્રફુલ પટેલની સિદ્ધિઓ તરફ નજર નાખીએ તો આ વિધાન કોઈ શંકા વિના સાચું પડે છે.</p>

					<p>સાચી વાત તો એ છે કે પ્રફુલ પટેલનો સમયગાળો ભારતીય સિવિલ એવિએશન માટે સુવર્ણ યુગ હતો એવું કહેવું એ તેમની સિદ્ધિઓને ઓછી આંકવા જેવું છે. આંકડાઓ પરથી જોઈ શકાય છે કે પ્રફુલ પટેલે હવાલો સંભાળ્યો ત્યારબાદ ડોમેસ્ટિક એર ટ્રાફિકમાં ત્રણ ગણો વધારો થયો હતો. તેમના દ્વારા લાવવામાં આવેલા સુધારા દેશના એવિએશન ક્ષેત્ર માટે એટલા લાભદાયી નીવડ્યા કે આંતરરાષ્ટ્રીય ટ્રાફિકમાં પણ જંગી વધારો થયો હતો. 2004માં સિવિલ એવિએશન માટે ભારત પાસે કુલ 125 વિમાન હતા, પરંતુ પ્રફુલભાઈએ ચાર્જ સંભાળ્યા પછી ભારતીય સિવિલ એવિએશન સેક્ટર પાસે હાલ 400 કરતાં વધુ વિમાન છે.</p>
					
					<p>આ ઉપરાંત તેમણે દેશમાં વિમાનમથકો ઉપર પણ ધ્યાન કેન્દ્રિત કર્યું જેથી દેશના ઉડ્ડયનક્ષેત્રને ઘણો લાભ થયો. કેટલાક લોકોના તીવ્ર વિરોધ છતાં તેમણે દિલ્હી, મુંબઈ, હૈદરાબાદ અને બેંગલોરના વિમાનમથકોનું ખાનગીકરણ કર્યું જેથી મોટાપાયે એક્સપાન્સન થયું અને ખાનગી ભાગીદારીથી મોટાપાયે મૂડીરોકાણ પણ આવ્યું.</p>

					<p>દિલ્હીમાં ઈન્દિરા ગાંધી એરપોર્ટનું ટી3 પ્રફુલ પટેલના વિઝનને કારણે જ આજે દુનિયાના સૌથી આધુનિક એરપોર્ટમાં સ્થાન પામ્યું છે. એ જ રીતે મુંબઈ ખાતે ટી2 આંતરરાષ્ટ્રીય ટર્મિનલ પણ વૈશ્વિક રેન્કિંગમાં સમાવેશ પામે છે. તેમના સાત વર્ષના ગાળા દરમિયાન દેશમાં તેમણે ઘણા એરપોર્ટ પ્રોજેક્ટ હાથ ધર્યા હતા અને પૂરા પણ કર્યા હતા. તેમના સમયગાળામાં જ દેશમાં આધુનિક વિમાનમથકોનું ઉદઘાટન થયું હતું. ખાસ કરીને હૈદરાબાદ ઈન્ટરનેશનલ એરપોર્ટ ભારતના ટોચના વિમાનમથકોમાં સ્થાન પામે છે એટલું જ નહીં પરંતુ યુકેસ્થિત એવિએશન રિસર્ચ ઓર્ગેનાઈઝેશન દ્વારા તેનો સમાવેશ 2013ના વર્લ્ડ એરપોર્ટ રેન્કિંગમાં થયો હતો. નાગરિક ઉડ્ડયન મંત્રાલયનો હવાલો સંભાળ્યાના થોડા મહિનામાં જ તેઓ સરકારને એ સમજાવવામાં સફળ રહ્યા હતા કે ડોમેસ્ટિક એરલાઈન સેક્ટરમાં એફડીઆઈની મર્યાદા 40 ટકાથી વધારીને 49 ટકા કરવી જોઈએ. દેશના એવિએશન ક્ષેત્રને જોમવંતુ બનાવવામાં પ્રફુલ પટેલે ભજવેલી ભૂમિકાને વ્યાપક સરાહના મળી છે.</p>

					<p>એક વ્યક્તિ એકલે હાથે આવું ગંજાવર કામ કેવી રીતે કરી શકે તે સમજવા માટે પ્રફુલ પટેલના પ્રારંભિક જીવન તરફ નજર નાખવી પડે. પ્રફુલ પટેલને ઘણી નાની વયમાં જ પિતાના વારસાની જવાબદારી મળી હતી. તેમના પર પરિવારનો બિઝનેસ સંભાળવાની અને સાથે સાથે મનોહરભાઈએ શરૂ કરેલાં સામાજિક સેવાનાં કામો આગળ ધપાવવાની જવાબદારી આવી હતી. વિદ્યાર્થીકાળથી જ પ્રફુલભાઈ મુંબઈમાં તેમની કેમ્પિયન સ્કૂલ તેમજ સિંધમ કૉલેજ અને ગોંદિયામાં કામગીરી – એ બંને વચ્ચે સંતુલન રાખતા હતા. એ કહેવાની જરૂર નથી કે તેમણે આ બધી જ જવાબદારી ફરજની ભાવના અને જવાબદારીપૂર્વક અદા કરી હતી.</p>

					<p>એ વાતનું કોઈને આશ્ચર્ય ન હોવું જોઈએ કે 1991માં માત્ર 33 વર્ષની ઉંમરે પ્રફુલ પટેલ પ્રથમ વખત લોકસભામાં ચૂંટાયા ત્યાર પહેલા જ તેમણે સક્ષમ વહીવટકર્તા અને ડાયમેનિક વ્યક્તિત્વનો પરિચય આપી દીધો હતો. રાષ્ટ્રવાદી કોંગ્રેસ પાર્ટીમાં તેમનું પ્રદાન નોંધપાત્ર છે અને તેમને નાગરિક ઉડ્ડયન પ્રધાન બનાવવામાં આવ્યા ત્યારે મોટાભાગનાને વિશ્વાસ હતો કે આ ક્ષેત્રમાં હવે મોટાપાયે ફેરફાર થશે… અને તેમણે એ કરી બતાવ્યું. જવાબદારી સંભાળ્યા પછી તેમણે અપેક્ષા પ્રમાણેના પરિણામ પણ આપ્યા જે આજે પણ જોઈ અને અનુભવી શકાય છે. તેમણે કરેલા સુધારાને પગલે એર ટ્રાફિકમાં વધારો થયો અને એ બાબત પણ પડકારજનક બની રહી હતી. પરંતુ તેમણે એ પડકાર ઝીલી લીધા અને વિઝન સાથે એક પ્રગતિશીલ નેતા તરીકે ઉભરી આવ્યા. સમગ્ર એર ટ્રાફિક સિસ્ટમને અપગ્રેડ અને મજબૂત કરવામાં તેમણે ચાવીરૂપ ભૂમિકા ભજવી. આજે, ભારત પાસે અત્યંત આધુનિક એટીસી તથા સીએનએસ સિસ્ટમ છે જે પ્રફુલ પટેલને આભારી છે. સેટેલાઈટ આધારિત નેવિગેશન સિસ્ટમ ધરાવતા દેશોમાં ભારતનું સ્થાન ચોથું છે. તેમને સમયગાળામાં એરપોર્ટ ઑથોરિટી ઑફ ઈન્ડિયાએ ગગન પ્રોગ્રામ હેઠળ પોતાનો સેટેલાઈટ લૉન્ચ કર્યો હતો. દેશમાં સિવિલ એવિએશનની સ્થિતિ સુધાર્યા પછી પ્રફુલ પટેલને 2011ની 19 જાન્યુઆરીએ ભારે ઉદ્યોગો અને જાહેર સાહસોના મંત્રાલયનો હવાલો સોંપવામાં આવ્યો હતો અને ત્યાં પણ તેમણે મોટાપાયે સુધારા કર્યા હતા. તેમની આ ફેરબદલથી ભારતના એવિએશન ક્ષેત્ર માટે નુકસાન હતું તો બીજી તરફ ભારે ઉદ્યોગોના ક્ષેત્ર માટે લાભદાયી પણ હતી.</p>

					<p>2013ની 9 જાન્યુઆરીએ પ્રફુલ પટેલે નેશનલ ઈલેક્ટ્રીક મોબિલિટી મિશન (એનઈએમએમપી) 2020 શરૂ કર્યું હતું જે દેશમાં ઈલેક્ટ્રીક અને હાઈબ્રિડ વાહનો માટેનો એક અસરકારક કાર્યક્રમ છે. એનઈએમએમપી 2020 હેઠળ ભારતમાં ઈલેક્ટ્રીક વાહનોનો મોટાપાયે ઉપયોગ થશે, જે કૉસ્ટ એફિશિયન્ટ હશે તેમજ દેશની ઈંધણની આયાત ઓછી થતાં મોટાપાયે વિદેશી હુંડિયામણ પણ બચશે.</p>

					<p>જાહેર સાહસોના વિભાગ મારફત પ્રફુલ પટેલે જાહેરક્ષેત્ર તેમજ ખાનગી ક્ષેત્ર એમ બંને માટે સ્પર્ધાત્મક માર્કેટમાં સમાન તકો ઊભી કરી છે. તેમણે એક હાઈપાવર સમિતિની નિમણૂક કરી હતી જેણે ભવિષ્યમાં જાહેરક્ષેત્રની અગત્યની ભૂમિકા માટે સૂચનો કર્યાં હતાં.</p>

					<p>બીજા અનેક એવોર્ડ અને પુરસ્કારોની સાથે સાથે પ્રફુલ પટેલને આર્થિક સુધારાની દિશામાં આપેલા યોગદાન બદલ 2007માં પ્રતિષ્ઠિત લીડ્સ મેટ્રોપોલિટન યુનિવર્સિટી દ્વારા ડૉક્ટરેટની પદવી એનાયત કરવામાં આવી હતી. ઉપરાંત એ જ વર્ષે શ્રી પટેલને ઈકોનોમિક ટાઈમ્સ રિફોર્મર ઑફ યરનું સન્માન મળ્યું હતું. જોકે, તેમની સિદ્ધિઓ અંગે પૂછવામાં આવ્યું ત્યારે આ ડાયનેમિક યુવા નેતાએ મક્કમતાપૂર્વક કહ્યું હતું કે, “હું કંઈક કરવાનો નિર્ણય કરું તો એ થાય એ સુનિશ્ચિત કરું છું. હું સિદ્ધિને મહત્ત્વની ગણું છું, પરંતુ લોકોને મદદરૂપ થવું એ મારા માટે વધારે સંતોષજનક છે.” લોકોને સાથે લાવવાની અને કામગીરી કરવાની આ ભાવનાને કારણે જ તેમના ટીકાકારો અને શુભચ્છકો એકી અવાજે પ્રફુલ પટેલને તેમના રાજકીય આદર્શ એવા સરદાર વલ્લભભાઈ પટેલની સાથે સરખાવે છે.</p>

