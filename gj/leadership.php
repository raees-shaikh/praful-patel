					<h4 class="sub-head">પ્રફુલ પટેલઃ એક અસાધારણ નેતા</h4>
					<p>સંસ્કારી અને સુખી પરિવારમાં જન્મેલા પ્રફુલ પટેલ પોતે સત્તા અને વિનમ્રતાનું ઉમદા ઉદાહરણ છે. તેનો શ્રેય દેખીતી રીતે તેમના પિતા સ્વ. મનોહરભાઈ પટેલને જાય છે, જેમણે પોતે શરૂઆતમાં ઘણું સાધારણ જીવન વિતાવ્યું હતું. પરિવારની સાધારણ સ્થિતિને કારણે પિતા મનોહરભાઈ પટેલ પૂરતું શિક્ષણ મેળવી શક્યા નહોતા અને રોજગારીની શોધમાં વતન ગુજરાત પણ છોડ્યું હતું. ટ્રક લોડિંગ-અનલોડિંગ કરવાથી માંડીને ગૃહ ઉદ્યોગની ચીજો સાઈકલ પર ફરીને વેચવા જેવાં કામો કરનાર મનોહરભાઈએ પછી ગોંદિયામાં બીડી ઉદ્યોગમાં ઝંપલાવ્યું અને એક સફળ બિઝનેસમેન તરીકે આગળ આવ્યા. પોતે માત્ર ચાર ધોરણ સુધી ભણ્યા હોવાને કારણે મનોહરભાઈએ જીવનમાં સફળતા મેળવ્યા પછી મુખ્ય ધ્યેય શિક્ષણના ક્ષેત્રમાં કામગીરી કરવાનું રાખ્યું. આ તમામ કામગીરીમાં તેમને પત્ની શાંતાબેનને પૂરેપૂરો સહકાર મળી રહ્યો જેને કારણે તેઓ સફળતાનાં શિખરો સર કરી શક્યા.પોતાના જીવનની આ બે મહત્ત્વની વ્યક્તિ- માતા-પિતા પાસેથી ઉચ્ચ અને માનવીય મૂલ્યો ગ્રહણ કરનાર પ્રફુલ પટેલે કેન્દ્રીય પ્રધાન સહિત અનેક ઉચ્ચ હોદ્દા પર સેવા બજાવી હોવા છતાં સામાન્ય પ્રજાજનો સાથેનો આત્મીય સંબંધ યથાવત્ જાળવી રાખ્યો છે. ડાયનેમિક વ્યક્તિત્વ ધરાવતા પ્રફુલ પટેલને નિયમિત રીતે ભંડારા અને ગોંદિયાના ખેડૂતો સાથે સાવ સહજતાથી વાતચીત કરતા જોઈ શકાય છે.</p>
				</div>
				<div class="q-col-1-1">
				<p> પિતા તરફથી નેતૃત્વના ગુણ વારસામાં મેળવનાર પ્રફુલભાઈએ ઘણી યુવાન વયે રાજકારણમાં ઝંપલાવ્યું હતું. સૌપ્રથમ 28 વર્ષની ઉંમરે તેઓ ગોંદિયા મ્યુનિસિપલ કાઉન્સિલના પ્રમુખ તરીકે ચૂંટાઈ આવ્યા હતા અને 33 વર્ષની ઉંમરે પ્રથમ વખત લોકસભામાં ચૂંટાયા હતા. લોકસભામાં સળંગ ત્રણ મુદત માટે ચૂંટાયા પછી વર્ષ 2000 અને 2006 એમ બે વખત રાજ્યસભા માટે ચૂંટાયા હતા. આ પછી 2009માં ચોથી વખત તેઓ લોકસભામાં ચૂંટાયા હતા. તે સમયે ભારતીય પ્રધાનમંડળના જે જૂજ યુવાન નેતાઓનો સમાવેશ કરવામાં આવ્યો હતો તેમાં પ્રફુલભાઈનો પણ સમાવેશ થતો હતો.</p>
