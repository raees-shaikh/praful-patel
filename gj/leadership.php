					<p>પ્રફુલભાઈએ તેમના નેતા અને ભારતના શક્તિશાળી રાજકારણીઓ પૈકી એક એવા શ્રી શરદ પવારને સક્રિય ટેકો આપીને તેમને મજબૂત બનાવ્યા છે. શ્રી પવારની સાથે તેમણે પોતાની રાજકીય કારકિર્દીના ઉતાર-ચઢાવનો મક્કમતાપૂર્વક સામનો કર્યો છે એટલું જ નહીં પરંતુ શ્રદ્ધા અને વિશ્વાસ સાથે સંબંધો વધારે મજબૂત બનાવ્યા છે. એનસીપીનો પાયાનો સિદ્ધાંત પ્રગતિ, વિકાસ અને સેક્યુલારિઝમ (ધર્મનિરપેક્ષતા) છે. એનસીપીને રાષ્ટ્રીય સ્તરે ફેલાવવામાં પ્રફુલ પટેલનો ફાળો નોંધપાત્ર છે અને એ બાબત તેમની રાજકીય કૂનેહનું ઝળહળતું ઉદાહરણ છે.</p>
